<h1>The pokemon card auction homepage</h1>
<hr>

<h3>How it works</h3>
<p>We sell pokemon cards on this auction site, people can bid on them while the bid is still available or there hasn't been a bid selected yet by the owner.</p>

<h4 class="pt-3">Pokemon cards</h4>
<p>Anyone can view a pokemon card, there's also a search function available for looking up a pokemon based on it's name and the ability to filter pokemon cards based on some the following properties:</p>
<ul>
    <li class="mb-2"><span class="font-monospace bg-light px-2 py-1 fw-bold">Rarity</span></li>
    <li class="mb-2"><span class="font-monospace bg-light px-2 py-1 fw-bold">Element</span></li>
    <li class="mb-2"><span class="font-monospace bg-light px-2 py-1 fw-bold">Weakness</span></li>
    <li class="mb-2"><span class="font-monospace bg-light px-2 py-1 fw-bold">Resistance</span></li>
</ul>

<h4 class="pt-3">Bids</h4>
<p>Anyone can see the bids of a certain pokemon card. If someone wants to place an actual bid they will have to be a registered user that is logged in.</p>
<p>Admins of the auction site can select the currently highest bid on a certain pokemon card and declare it the winner.
Once that happens te auction will still be available but users will no longer be able to place or remove bids.</p>

